        private void loadLoanProductConfig(String productName) {
        try (Connection conn = ConnectionManager.getConnection()) {
            String query = "SELECT * FROM loan_products WHERE name = ?";
            PreparedStatement stmt = conn.prepareStatement(query);
            stmt.setString(1, productName);
            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                interestRateField.setText(String.valueOf(rs.getDouble("interest_rate")));
                minAmountField.setText(String.valueOf(rs.getDouble("min_amount")));
                maxAmountField.setText(String.valueOf(rs.getDouble("max_amount")));
                minMonthsField.setText(String.valueOf(rs.getInt("min_term")));
                maxMonthsField.setText(String.valueOf(rs.getInt("max_term")));
                allowVIPCheckBox.setSelected(rs.getBoolean("allow_vip"));
                allowRegularCheckBox.setSelected(rs.getBoolean("allow_regular"));
                allowNewCheckBox.setSelected(rs.getBoolean("allow_new"));
                cleanHistoryCheckBox.setSelected(rs.getBoolean("require_clean_history"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading product config: " + e.getMessage());
        }
    }

