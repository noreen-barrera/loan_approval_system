    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        if (
            interestRateField.getText().trim().isEmpty() ||
            minAmountField.getText().trim().isEmpty() ||
            maxAmountField.getText().trim().isEmpty() ||
            minMonthsField.getText().trim().isEmpty() ||
            maxMonthsField.getText().trim().isEmpty()
        ) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;
        }

        try {
            String productName = (String) productTypeComboBox.getSelectedItem();
            double rate = Double.parseDouble(interestRateField.getText().trim());
            double minAmount = Double.parseDouble(minAmountField.getText().trim());
            double maxAmount = Double.parseDouble(maxAmountField.getText().trim());
            int minTerm = Integer.parseInt(minMonthsField.getText().trim());
            int maxTerm = Integer.parseInt(maxMonthsField.getText().trim());

            boolean vipBox = allowVIPCheckBox.isSelected();
            boolean regularBox = allowRegularCheckBox.isSelected();
            boolean newBox = allowNewCheckBox.isSelected();
            boolean cleanHistory = cleanHistoryCheckBox.isSelected();

            Connection conn = ConnectionManager.getConnection();
            String sql = """
                UPDATE loan_products SET 
                    interest_rate = ?, min_amount = ?, max_amount = ?, 
                    min_term = ?, max_term = ?, 
                    allow_vip = ?, allow_regular = ?, allow_new = ?, 
                    require_clean_history = ?
                WHERE name = ?
            """;

            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setDouble(1, rate);
            pstmt.setDouble(2, minAmount);
            pstmt.setDouble(3, maxAmount);
            pstmt.setInt(4, minTerm);
            pstmt.setInt(5, maxTerm);
            pstmt.setBoolean(6, vipBox);
            pstmt.setBoolean(7, regularBox);
            pstmt.setBoolean(8, newBox);
            pstmt.setBoolean(9, cleanHistory);
            pstmt.setString(10, productName);

            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Product configuration updated!");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid numbers.");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
        }
    }//GEN-LAST:event_saveButtonActionPerformed


